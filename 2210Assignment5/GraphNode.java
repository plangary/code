public class GraphNode {

    private int name;
    private boolean mark;

    public GraphNode (int name){
        this.name = name;
        this.mark = false;
    }

    public void setMark(boolean mark){
        this.mark = mark;

    }

    public boolean getMark(){
        return mark;
    }

    public int getName(){
        return name;
    }

















}
